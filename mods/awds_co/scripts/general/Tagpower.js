TAGPOWER.getTagname = function(co1, co2)
{
    var coid1 = co1.getCoID();
    var coid2 = co2.getCoID();
    for (var i = 0; i < TAGPOWER.TAGPOWERDATA.length; i++)
    {
        if ((TAGPOWER.TAGPOWERDATA[i][0] === coid1 && TAGPOWER.TAGPOWERDATA[i][1] === coid2) ||
            (TAGPOWER.TAGPOWERDATA[i][0] === coid2 && TAGPOWER.TAGPOWERDATA[i][1] === coid1))
        {
            return TAGPOWER.TAGPOWERDATA[i][2];
        }
    }
    return qsTr("Tagpower");
};
